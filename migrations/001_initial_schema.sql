-- INIT: v_collectible_loans
CREATE VIEW IF NOT EXISTS v_collectible_loans AS
SELECT * FROM accepted_loans
WHERE loan_status IN (
	'Charged Off',
	'Default',
	'Late (31-120 days)',
	'Late (16-30 days)',
	'In Grace Period',
	'Does not meet the credit policy. Status:Charged Off'
);

